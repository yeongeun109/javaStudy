package algo_0201;

import java.util.Scanner;

public class bj_17478_재귀함수가뭔가요 {

	static int n;
	
	public static void underBar(int a, StringBuilder sb) {
		for(int i = 0; i < (n - a) * 4; i++) {
			sb.append("_");
		}
	}
	
	public static void chatBot(int a) {
		StringBuilder sb = new StringBuilder();
		
		if(a == 0) {
			StringBuilder sb3 = new StringBuilder();
			underBar(a, sb3);
			sb3.append("\"재귀함수가 뭔가요?\"\n");
			underBar(a, sb3);
			sb3.append("\"재귀함수는 자기 자신을 호출하는 함수라네\"\n");
			underBar(a, sb3);
			sb3.append("라고 답변하였지.\n");
			System.out.print(sb3);
			return;
		}
		
		underBar(a, sb);
		sb.append("\"재귀함수가 뭔가요?\"\n");
		underBar(a, sb);
		sb.append("\"잘 들어보게. 옛날옛날 한 산 꼭대기에 이세상 모든 지식을 통달한 선인이 있었어.\n");
		underBar(a, sb);
		sb.append("마을 사람들은 모두 그 선인에게 수많은 질문을 했고, 모두 지혜롭게 대답해 주었지.\n");
		underBar(a, sb);
		sb.append("그의 답은 대부분 옳았다고 하네. 그런데 어느 날, 그 선인에게 한 선비가 찾아와서 물었어.\"\n");
		System.out.print(sb);
		chatBot(a-1);
		
		StringBuilder sb2 = new StringBuilder();
		underBar(a, sb2);
		sb2.append("라고 답변하였지.");
		System.out.println(sb2);
	}
	
	public static void main(String[] args) {
		Scanner sc = new Scanner(System.in);
		n = sc.nextInt();
		System.out.println("어느 한 컴퓨터공학과 학생이 유명한 교수님을 찾아가 물었다.");
		
		chatBot(n);
	}

}
